int FreqFromMidiNote(int midiNote) {  
  for(int i = 0; i < sizeof(noteLookup); i++) {
    if(noteLookup[i].midiNote == midiNote) {
      return noteLookup[i].frequency;
    }
  }
  
  return 0;
}
